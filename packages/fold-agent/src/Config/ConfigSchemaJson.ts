/** Options for {@link writeFoldConfigSchema} and {@link configInit}. */
export type ConfigInitOptions = {
	/** The fold home directory. Defaults to `~/.fold`. */
	readonly foldHome?: string
	/** FileSystem override for hermetic tests. Defaults to the Node platform filesystem. */
	readonly fileSystem?: FsToolOptions['fileSystem']
}

/** Write `config.schema.json` under the fold home, creating the directory if needed. Returns its path. */
export const writeFoldConfigSchema = (options?: ConfigInitOptions): Effect.Effect<string> =>
	Effect.gen(function* () {
		const fs = fileSystemFor(options?.fileSystem === undefined ? {} : { fileSystem: options.fileSystem })
		const home = options?.foldHome ?? defaultFoldHome()
		yield* fs.makeDirectory(home, { recursive: true }).pipe(Effect.orDie)

		const path = configSchemaPath(options?.foldHome)
		yield* fs.writeFileString(path, foldConfigJsonSchemaText()).pipe(Effect.orDie)
		return path
	})

/** Result of {@link bootstrapFoldHome} / {@link configInit}. */
export type ConfigInitResult = {
	readonly configPath: string
	readonly schemaPath: string
	/** Path of the generated FOLD_INFO.md guide (always rewritten). */
	readonly infoPath: string
	/** Path of the OAuth credential store (created EMPTY when absent; filled by `foldcode auth codex login`). */
	readonly authPath: string
	/** True when a starter `config.jsonc` was created; false when one already existed (left untouched). */
	readonly createdConfig: boolean
	/** True when an empty `auth.json` was created; false when one already existed (left untouched). */
	readonly createdAuth: boolean
}

/**
 * Bootstrap the fold home. Runs on EVERY CLI session start (synchronously, before the config loads)
 * and behind `foldcode config init`: create the directory, (re)write the version-matched
 * `config.schema.json` and `FOLD_INFO.md`, write the commented starter `config.jsonc` only when none
 * exists, and create an EMPTY provider-keyed `auth.json` (mode 0600) only when none exists - a first
 * run lands a working layout with providers configured but credential-less until
 * `foldcode auth codex login` runs or the `apiKeyEnv` variables are exported. Only the two generated
 * files are ever overwritten.
 */
export const bootstrapFoldHome = (options?: ConfigInitOptions): Effect.Effect<ConfigInitResult> =>
	Effect.gen(function* () {
		const fs = fileSystemFor(options?.fileSystem === undefined ? {} : { fileSystem: options.fileSystem })
		const schemaPath = yield* writeFoldConfigSchema(options)
		const infoPath = yield* writeFoldInfo(options)

		const configPath = defaultConfigPath(options?.foldHome)
		const configExists = yield* fs.exists(configPath).pipe(Effect.catch(() => Effect.succeed(false)))
		if (!configExists) {
			yield* fs.writeFileString(configPath, starterConfigJsonc()).pipe(Effect.orDie)
		}

		const authPath = join(options?.foldHome ?? defaultFoldHome(), 'auth.json')
		const authExists = yield* fs.exists(authPath).pipe(Effect.catch(() => Effect.succeed(false)))
		if (!authExists) {
			// Same document shape and permissions the codex auth store maintains (provider-keyed JSON).
			yield* fs.writeFileString(authPath, '{}\n', { mode: 0o600 }).pipe(Effect.orDie)
			yield* fs.chmod(authPath, 0o600).pipe(Effect.catch(() => Effect.void))
		}

		return { configPath, schemaPath, infoPath, authPath, createdConfig: !configExists, createdAuth: !authExists }
	})

/** `foldcode config init`: the same bootstrap, kept under its command-facing name. */
export const configInit = (options?: ConfigInitOptions): Effect.Effect<ConfigInitResult> => bootstrapFoldHome(options)
