/**
 * This file materializes `~/.fold/config.schema.json` from the SAME `FoldConfig` Effect Schema that
 * decodes the file (D25) - so editor validation/completion is always version-matched to the installed
 * fold. Generation goes through `Schema.toJsonSchemaDocument` (draft 2020-12) and
 * `JsonSchema.toDocumentDraft07` (editor compat), then assembles a self-contained document with the
 * draft-07 `$schema` URI and inlined `definitions`.
 *
 * `configInit` writes the schema (always, to stay current) and a commented starter `config.jsonc` (only
 * when absent, never clobbering an existing config) - the `foldcode config init` primitive.
 */
import { join } from 'node:path'

import { JsonSchema, Effect, Schema } from 'effect'

import { fileSystemFor, type FsToolOptions } from '../Fs/DefaultFileSystem'
import { FoldConfig } from './ConfigSchema'
import { writeFoldInfo } from './FoldInfo'
import { defaultConfigPath, defaultFoldHome } from './Load'

/** The generated schema file path for a home directory. */
export const configSchemaPath = (foldHome?: string): string => join(foldHome ?? defaultFoldHome(), 'config.schema.json')

/** The `FoldConfig` JSON Schema as a self-contained draft-07 document object (ready to serialize). */
export const foldConfigJsonSchema = (): Record<string, unknown> => {
	const document = JsonSchema.toDocumentDraft07(Schema.toJsonSchemaDocument(FoldConfig))
	const hasDefinitions = Object.keys(document.definitions).length > 0

	return {
		$schema: JsonSchema.META_SCHEMA_URI_DRAFT_07,
		...document.schema,
		...(hasDefinitions ? { definitions: document.definitions } : {}),
	}
}

/** The generated schema serialized as JSON text (tab-indented, trailing newline). */
export const foldConfigJsonSchemaText = (): string => `${JSON.stringify(foldConfigJsonSchema(), null, '\t')}\n`

